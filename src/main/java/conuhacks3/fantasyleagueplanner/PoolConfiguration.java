package conuhacks3.fantasyleagueplanner;

public class PoolConfiguration {
    private int attackerAssists;
    private int attackerGoals;
    private int defenderAssists;
    private int defenderGoals;

    public int getAttackerAssists() {
        return attackerAssists;
    }

    public void setAttackerAssists(int attackerAssists) {
        this.attackerAssists = attackerAssists;
    }

    public int getAttackerGoals() {
        return attackerGoals;
    }

    public void setAttackerGoals(int attackerGoals) {
        this.attackerGoals = attackerGoals;
    }

    public int getDefenderAssists() {
        return defenderAssists;
    }

    public void setDefenderAssists(int defenderAssists) {
        this.defenderAssists = defenderAssists;
    }

    public int getDefenderGoals() {
        return defenderGoals;
    }

    public void setDefenderGoals(int defenderGoals) {
        this.defenderGoals = defenderGoals;
    }
}
